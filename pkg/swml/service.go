// RoutingCallback is a function called on incoming requests to customize responses.
// It receives the request and request body, and returns an optional SWML JSON override.
// If it returns nil, the default document is used.
type RoutingCallback func(r *http.Request, body map[string]any) map[string]any

// VerbHandler is the interface for custom SWML verb handlers.
// Implement this interface and pass instances to RegisterVerbHandler to
// add custom validation or build logic for a verb. This mirrors
// Python's SWMLVerbHandler interface.
type VerbHandler interface {
	// GetVerbName returns the SWML verb name this handler manages.
	GetVerbName() string
	// ValidateConfig validates a verb config map and returns (isValid, errorMessages).
	ValidateConfig(config map[string]any) (bool, []string)
	// BuildConfig constructs a verb config map from keyword-style options.
	BuildConfig(opts map[string]any) map[string]any
}

// SecurityConfig bundles configuration for all supported authentication methods:
// HTTP Basic Auth, Bearer token, and API key. Pass it to WithSecurityConfig to
// configure multiple auth methods in one call.
//
// Fields default to the zero value (disabled). Set only the fields you need.
//
//   - BasicAuthUser / BasicAuthPassword — HTTP Basic Auth credentials.
//   - BearerToken — Expected value of the "Authorization: Bearer <token>" header.
//   - APIKey — Expected API key value.
//   - APIKeyHeader — Header name to read the API key from (default "X-API-Key").
//
// Mirrors Python's SecurityConfig dataclass.
type SecurityConfig struct {
	BasicAuthUser     string
	BasicAuthPassword string
	BearerToken       string
	APIKey            string
	APIKeyHeader      string // defaults to "X-API-Key" when APIKey is set
}

// Service is the base SWML service that manages documents, HTTP endpoints, and auth.
// It provides auto-vivified verb methods driven by the SWML schema.
type Service struct {
	mu sync.RWMutex

	Name   string
	Route  string
	Host   string
	Port   int
	Domain string // Domain for TLS certificates and URL generation (SWML_DOMAIN env var)
	Logger *logging.Logger

	// Auth
	basicAuthUser     string
	basicAuthPassword string
	// basicAuthAutoGenerated is set to true when the password was neither
	// provided explicitly nor sourced from SWML_BASIC_AUTH_PASSWORD. The
	// constructor logs a warning in that case so external callers know why
	// they are getting HTTP 401.
	basicAuthAutoGenerated bool

	// Bearer token authentication (optional; empty = disabled)
	bearerToken string

	// API key authentication (optional; both must be set to enable)
	apiKey       string
	apiKeyHeader string // header name, e.g. "X-API-Key"

	// TLS / SSL
	tlsCertFile string // path to TLS certificate file
	tlsKeyFile  string // path to TLS key file

	// Schema validation
	// schemaValidation controls whether verb schema validation is active.
	// Mirrors Python's schema_validation constructor param and
	// SWML_SKIP_SCHEMA_VALIDATION=1 env var.
	schemaValidation bool
	// schemaPath is an optional override path for the schema JSON file.
	// When empty the embedded schema is used (default). Mirrors Python's
	// schema_path constructor parameter.
	schemaPath string

	// SWML document
	document *Document

	// Schema for verb validation
	schema *Schema

	// Custom verb handlers (name → handler). Checked before schema validation.
	verbHandlers map[string]VerbHandler

	// Proxy detection
	proxyURLBase string

	// Routing callbacks
	routingCallbacks map[string]RoutingCallback

	// Server state
	server  *http.Server
	running bool

	// Verb method cache: maps verb name to whether it exists in schema
	verbCache map[string]bool

	// verbHandlers holds pluggable handlers registered by callers for custom
	// or extended SWML verbs. Keyed by the verb name returned by VerbHandler.GetVerbName().
	verbHandlers map[string]VerbHandler
}

// ServiceOption is a functional option for configuring a Service.
type ServiceOption func(*Service)

// WithName sets the service name.
func WithName(name string) ServiceOption {
	return func(s *Service) { s.Name = name }
}

// WithRoute sets the HTTP route path.
func WithRoute(route string) ServiceOption {
	return func(s *Service) { s.Route = strings.TrimRight(route, "/") }
}

// WithHost sets the HTTP server bind host.
func WithHost(host string) ServiceOption {
	return func(s *Service) { s.Host = host }
}

// WithPort sets the HTTP server port.
func WithPort(port int) ServiceOption {
	return func(s *Service) { s.Port = port }
}

// WithBasicAuth sets explicit basic auth credentials.
func WithBasicAuth(user, password string) ServiceOption {
	return func(s *Service) {
		s.basicAuthUser = user
		s.basicAuthPassword = password
	}
}

// WithBearerToken configures Bearer token authentication. When set, the
// withSecurity middleware accepts requests carrying an
// "Authorization: Bearer <token>" header that matches token in addition to
// (or instead of) Basic Auth.
// Mirrors Python's SecurityConfig.bearer_token field and the AuthHandler
// verify_bearer_token / flask_decorator behaviour.
func WithBearerToken(token string) ServiceOption {
	return func(s *Service) { s.bearerToken = token }
}

// WithAPIKey configures API key authentication. When set, the withSecurity
// middleware accepts requests where the header named by header equals key.
// header is the HTTP header name (e.g. "X-API-Key"); pass an empty string to
// use the default "X-API-Key".
// Mirrors Python's SecurityConfig.api_key / api_key_header fields and the
// AuthHandler verify_api_key / flask_decorator behaviour.
func WithAPIKey(key, header string) ServiceOption {
	return func(s *Service) {
		s.apiKey = key
		if header == "" {
			header = "X-API-Key"
		}
		s.apiKeyHeader = header
	}
}

// WithSecurityConfig applies a SecurityConfig bundle, setting Basic Auth,
// Bearer token, and API key configuration in one call.
// This is the Go equivalent of Python's AuthHandler(security_config=...) pattern:
// it maps each SecurityConfig field to the corresponding WithXxx option.
func WithSecurityConfig(cfg SecurityConfig) ServiceOption {
	return func(s *Service) {
		if cfg.BasicAuthUser != "" {
			s.basicAuthUser = cfg.BasicAuthUser
		}
		if cfg.BasicAuthPassword != "" {
			s.basicAuthPassword = cfg.BasicAuthPassword
		}
		if cfg.BearerToken != "" {
			s.bearerToken = cfg.BearerToken
		}
		if cfg.APIKey != "" {
			s.apiKey = cfg.APIKey
			hdr := cfg.APIKeyHeader
			if hdr == "" {
				hdr = "X-API-Key"
			}
			s.apiKeyHeader = hdr
		}
	}
}

