// ExtractSIPUsername extracts a SIP username from a request body.
func ExtractSIPUsername(body map[string]any) string {
	// Look in call.to field for SIP URI
	callData, ok := body["call"].(map[string]any)
	if !ok {
		return ""
	}
	to, ok := callData["to"].(string)
	if !ok {
		return ""
	}
	// Parse SIP URI: sip:username@domain
	if strings.HasPrefix(to, "sip:") {
		to = to[4:]
	}
	if idx := strings.Index(to, "@"); idx > 0 {
		return to[:idx]
	}
	return to
}

// --- Helpers ---

// filterNilValues removes nil values from a map, matching Python's behavior
// of skipping None kwargs in verb methods.
func filterNilValues(m map[string]any) map[string]any {
	if m == nil {
		return map[string]any{}
	}
	result := make(map[string]any, len(m))
	for k, v := range m {
		if v != nil {
			result[k] = v
		}
	}
	return result
}

// insertAuth inserts basic auth credentials into a URL.
func insertAuth(baseURL, user, password string) string {
	// Insert user:pass@ after the scheme://
	if idx := strings.Index(baseURL, "://"); idx >= 0 {
		scheme := baseURL[:idx+3]
		rest := baseURL[idx+3:]
		return fmt.Sprintf("%s%s:%s@%s", scheme, user, password, rest)
	}
	return baseURL
}

// Serve starts the HTTP server. This is a blocking call.
// When TLS is configured via WithTLS (or SWML_SSL_CERT_PATH / SWML_SSL_KEY_PATH
// env vars), ListenAndServeTLS is called automatically.
// Mirrors Python's serve() ssl_cert / ssl_key / ssl_enabled parameter support.
func (s *Service) Serve() error {
	mux := s.buildMux()
	addr := fmt.Sprintf("%s:%d", s.Host, s.Port)

	s.mu.Lock()
	s.server = &http.Server{
		Addr:    addr,
		Handler: mux,
	}
	if s.TLSEnabled() {
		// Require TLS 1.2+ (mirrors Python's CERT_REQUIRED ssl_verify_mode default).
		s.server.TLSConfig = &tls.Config{
			MinVersion: tls.VersionTLS12,
		}
	}
	s.running = true
	s.mu.Unlock()

	scheme := "http"
	if s.TLSEnabled() {
		scheme = "https"
	}
	s.Logger.Info("serving on %s://%s%s", scheme, addr, s.Route)
	s.Logger.Info("auth user: %s", s.basicAuthUser)

	if s.TLSEnabled() {
		s.Logger.Info("TLS enabled: cert=%s key=%s", s.tlsCertFile, s.tlsKeyFile)
		return s.server.ListenAndServeTLS(s.tlsCertFile, s.tlsKeyFile)
	}
	return s.server.ListenAndServe()
}

// Stop gracefully stops the HTTP server.
func (s *Service) Stop() error {
	s.mu.Lock()
	defer s.mu.Unlock()
	if s.server != nil {
		s.running = false
		return s.server.Close()
	}
	return nil
}

// buildMux creates the HTTP handler with auth middleware and routes.
func (s *Service) buildMux() *http.ServeMux {
	mux := http.NewServeMux()

	// Health endpoints (no auth)
	mux.HandleFunc("/health", func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Content-Type", "application/json")
		json.NewEncoder(w).Encode(map[string]string{"status": "healthy"})
	})
	mux.HandleFunc("/ready", func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Content-Type", "application/json")
		json.NewEncoder(w).Encode(map[string]string{"status": "ready"})
	})

	// Main SWML endpoint (with auth)
	route := s.Route
	if route == "" {
		route = "/"
	}
	mux.HandleFunc(route, s.withSecurity(s.handleSWML))

	return mux
}

// maxRequestBody is the maximum allowed request body size (1MB).
const maxRequestBody = 1 << 20

// handleSWML serves the SWML document.
func (s *Service) handleSWML(w http.ResponseWriter, r *http.Request) {
	var body map[string]any
	if r.Method == http.MethodPost {
		r.Body = http.MaxBytesReader(w, r.Body, maxRequestBody)
		json.NewDecoder(r.Body).Decode(&body)
	}

	doc := s.OnRequest(body, r.URL.Path)

	w.Header().Set("Content-Type", "application/json")
	json.NewEncoder(w).Encode(doc)
}

// withSecurity wraps a handler with auth and security headers.
// withSecurity wraps a handler with security headers and multi-method auth.
//
// Auth check order (mirrors Python AuthHandler.flask_decorator / get_fastapi_dependency):
//  1. Bearer token — "Authorization: Bearer <token>" header (if configured).
//  2. API key — configured header name (if configured).
//  3. HTTP Basic Auth — always checked as the final fallback.
//
// A request is authenticated when ANY of the configured methods matches. The
// previous behaviour (Basic Auth only) is preserved for services that do not
// configure bearer or API key auth.
func (s *Service) withSecurity(next http.HandlerFunc) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		// Security headers
		w.Header().Set("X-Content-Type-Options", "nosniff")
		w.Header().Set("X-Frame-Options", "DENY")
		w.Header().Set("Cache-Control", "no-store")

		s.mu.RLock()
		bearerToken := s.bearerToken
		apiKey := s.apiKey
		apiKeyHeader := s.apiKeyHeader
		basicUser := s.basicAuthUser
		basicPass := s.basicAuthPassword
		s.mu.RUnlock()

		// 1. Try Bearer token (if configured)
		if bearerToken != "" {
			authHdr := r.Header.Get("Authorization")
			if strings.HasPrefix(authHdr, "Bearer ") {
				token := authHdr[len("Bearer "):]
				if subtle.ConstantTimeCompare([]byte(token), []byte(bearerToken)) == 1 {
					next(w, r)
					return
				}
			}
		}

		// 2. Try API key (if configured)
		if apiKey != "" {
			hdr := apiKeyHeader
			if hdr == "" {
				hdr = "X-API-Key"
			}
			if k := r.Header.Get(hdr); k != "" {
				if subtle.ConstantTimeCompare([]byte(k), []byte(apiKey)) == 1 {
					next(w, r)
					return
				}
			}
		}

		// 3. Basic auth fallback (timing-safe comparison)
		user, pass, ok := r.BasicAuth()
		userMatch := subtle.ConstantTimeCompare([]byte(user), []byte(basicUser)) == 1
		passMatch := subtle.ConstantTimeCompare([]byte(pass), []byte(basicPass)) == 1
		if ok && userMatch && passMatch {
			next(w, r)
			return
		}

		w.Header().Set("WWW-Authenticate", `Basic realm="SWML Service"`)
		http.Error(w, "Unauthorized", http.StatusUnauthorized)
	}
}
