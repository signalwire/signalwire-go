// NewService creates a new SWML service with the given options.
func NewService(opts ...ServiceOption) *Service {
	s := &Service{
		Name:             "SWML",
		Route:            "/",
		Host:             "0.0.0.0",
		Port:             3000,
		Logger:           logging.New("swml-service"),
		document:         NewDocument(),
		routingCallbacks: make(map[string]RoutingCallback),
		verbCache:        make(map[string]bool),
		verbHandlers:     make(map[string]VerbHandler),
		// Schema validation is on by default (matches Python default True).
		// Can be overridden by WithSchemaValidation(false) or
		// SWML_SKIP_SCHEMA_VALIDATION=1.
		schemaValidation: true,
	}

	// Apply options
	for _, opt := range opts {
		opt(s)
	}

	// Honour SWML_SKIP_SCHEMA_VALIDATION=1 env var (mirrors Python behaviour).
	if os.Getenv("SWML_SKIP_SCHEMA_VALIDATION") == "1" {
		s.schemaValidation = false
	}

	// TLS domain from env (mirrors Python's SWML_DOMAIN / SecurityConfig.domain).
	if s.Domain == "" {
		s.Domain = os.Getenv("SWML_DOMAIN")
	}
	// TLS cert/key from env when not set via WithTLS (mirrors SWML_SSL_CERT_PATH / SWML_SSL_KEY_PATH).
	if s.tlsCertFile == "" {
		s.tlsCertFile = os.Getenv("SWML_SSL_CERT_PATH")
	}
	if s.tlsKeyFile == "" {
		s.tlsKeyFile = os.Getenv("SWML_SSL_KEY_PATH")
	}

	// Port from env if not explicitly set
	if envPort := os.Getenv("PORT"); envPort != "" {
		var port int
		if _, err := fmt.Sscanf(envPort, "%d", &port); err == nil {
			s.Port = port
		}
	}

	// Auth from env if not explicitly set
	if s.basicAuthUser == "" {
		s.basicAuthUser = os.Getenv("SWML_BASIC_AUTH_USER")
	}
	if s.basicAuthPassword == "" {
		s.basicAuthPassword = os.Getenv("SWML_BASIC_AUTH_PASSWORD")
	}
	// Auto-generate if still empty
	if s.basicAuthUser == "" {
		s.basicAuthUser = s.Name
	}
	if s.basicAuthPassword == "" {
		s.basicAuthPassword = generatePassword()
		s.basicAuthAutoGenerated = true
		// Warn loudly so external callers (tests, RPC clients, MCP) know
		// why they are getting HTTP 401. This is the silent cause of every
		// external caller failing when .env wasn't loaded.
		s.Logger.Warn(
			"basic_auth_password_autogenerated: username=%q. "+
				"No SWML_BASIC_AUTH_PASSWORD found in environment and no "+
				"password passed via WithBasicAuth. The SDK generated a "+
				"random password that exists only in this process; external "+
				"callers will get HTTP 401 unless they read the value from "+
				"this process's env. To fix, set SWML_BASIC_AUTH_USER and "+
				"SWML_BASIC_AUTH_PASSWORD in your environment, or pass "+
				"swml.WithBasicAuth(user, pass) / agent.WithBasicAuth(...) "+
				"when constructing the service.",
			s.basicAuthUser,
		)
	}

	// Proxy URL from env
	s.proxyURLBase = os.Getenv("SWML_PROXY_URL_BASE")

	// Load schema — only when validation is enabled.
	if s.schemaValidation {
		var schema *Schema
		var err error
		if s.schemaPath != "" {
			schema, err = LoadSchemaFromFile(s.schemaPath)
		} else {
			schema, err = GetSchema()
		}
		if err != nil {
			s.Logger.Warn("failed to load SWML schema: %s", err)
		} else {
			s.schema = schema
			s.Logger.Debug("loaded schema with %d verbs", schema.VerbCount())
		}
	}

	// Register built-in verb handlers (mirrors Python VerbHandlerRegistry.__init__).
	s.RegisterVerbHandler(NewAIVerbHandler())

	return s
}

// generatePassword creates a random password for auto-generated basic auth.
// Panics if the system has no entropy source — never fall back to a weak password.
func generatePassword() string {
	bytes := make([]byte, 16)
	if _, err := rand.Read(bytes); err != nil {
		panic("crypto/rand failed: cannot generate secure password: " + err.Error())
	}
	return hex.EncodeToString(bytes)
}

// GetDocument returns the current SWML document.
func (s *Service) GetDocument() *Document {
	s.mu.RLock()
	defer s.mu.RUnlock()
	return s.document
}

// ResetDocument resets the SWML document to empty.
func (s *Service) ResetDocument() {
	s.mu.Lock()
	defer s.mu.Unlock()
	s.document = NewDocument()
}

// GetBasicAuthCredentials returns the (username, password) for basic auth.
func (s *Service) GetBasicAuthCredentials() (string, string) {
	return s.basicAuthUser, s.basicAuthPassword
}

// GetBasicAuthCredentialsWithSource returns (username, password, source) where
// source is one of "environment", "explicit", or "auto-generated".
// Mirrors Python's get_basic_auth_credentials(include_source=True) three-tuple return.
func (s *Service) GetBasicAuthCredentialsWithSource() (string, string, string) {
	user := s.basicAuthUser
	pass := s.basicAuthPassword

	envUser := os.Getenv("SWML_BASIC_AUTH_USER")
	envPass := os.Getenv("SWML_BASIC_AUTH_PASSWORD")

	var source string
	switch {
	case s.basicAuthAutoGenerated:
		source = "auto-generated"
	case envUser != "" && envPass != "" && envUser == user && envPass == pass:
		source = "environment"
	default:
		source = "explicit"
	}
	return user, pass, source
}

// VerifyBasicAuth performs a timing-safe comparison of the supplied credentials
// against the service's configured basic auth username and password.
// Returns true when both username and password match.
//
// This exposes the same check that withSecurity applies inside the HTTP
// middleware, making auth testable in isolation without an HTTP round-trip.
// Mirrors Python AuthHandler.verify_basic_auth(credentials) -> bool using
// secrets.compare_digest (equivalent: crypto/subtle.ConstantTimeCompare).
func (s *Service) VerifyBasicAuth(username, password string) bool {
	s.mu.RLock()
	defer s.mu.RUnlock()
	userMatch := subtle.ConstantTimeCompare([]byte(username), []byte(s.basicAuthUser)) == 1
	passMatch := subtle.ConstantTimeCompare([]byte(password), []byte(s.basicAuthPassword)) == 1
	return userMatch && passMatch
}

// VerifyBearerToken performs a timing-safe comparison of the supplied token
// against the service's configured bearer token.
// Returns false when no bearer token is configured (i.e. WithBearerToken was
// not called).
//
// Callers typically extract the token from the "Authorization: Bearer <token>"
// header before calling this method.
// Mirrors Python AuthHandler.verify_bearer_token(credentials) -> bool.
func (s *Service) VerifyBearerToken(token string) bool {
	s.mu.RLock()
	defer s.mu.RUnlock()
	if s.bearerToken == "" {
		return false
	}
	return subtle.ConstantTimeCompare([]byte(token), []byte(s.bearerToken)) == 1
}

// VerifyAPIKey performs a timing-safe comparison of the supplied key value
// against the service's configured API key.
// Returns false when no API key is configured (i.e. WithAPIKey was not called).
//
// Callers typically read the key from the header returned by APIKeyHeader().
// Mirrors Python AuthHandler.verify_api_key(api_key: str) -> bool.
func (s *Service) VerifyAPIKey(key string) bool {
	s.mu.RLock()
	defer s.mu.RUnlock()
	if s.apiKey == "" {
		return false
	}
	return subtle.ConstantTimeCompare([]byte(key), []byte(s.apiKey)) == 1
}

// GetAuthInfo returns a map describing every configured authentication method.
// The map mirrors the Python AuthHandler.get_auth_info() return value:
//
//   - "basic"   → {"enabled": true, "username": "<user>"}
//   - "bearer"  → {"enabled": true, "hint": "Use Authorization: Bearer <token>"}
//   - "api_key" → {"enabled": true, "header": "<hdr>", "hint": "Use <hdr>: <key>"}
//
// Only methods that are actively configured appear in the map. Basic auth is
// always present because the service always has a username/password (either
// explicit or auto-generated).
// Mirrors Python AuthHandler.get_auth_info() -> Dict[str, Any].
func (s *Service) GetAuthInfo() map[string]any {
	s.mu.RLock()
	defer s.mu.RUnlock()

	info := map[string]any{
		"basic": map[string]any{
			"enabled":  true,
			"username": s.basicAuthUser,
		},
	}

	if s.bearerToken != "" {
		info["bearer"] = map[string]any{
			"enabled": true,
			"hint":    "Use Authorization: Bearer <token>",
		}
	}

	if s.apiKey != "" {
		hdr := s.apiKeyHeader
		if hdr == "" {
			hdr = "X-API-Key"
		}
		info["api_key"] = map[string]any{
			"enabled": true,
			"header":  hdr,
			"hint":    "Use " + hdr + ": <key>",
		}
	}

	return info
}

