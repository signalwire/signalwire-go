// Command generate-relay-protocol emits the RELAY WS protocol types
// (pkg/relay/protocol_types_generated.go, package relay): one Go type per RELAY
// method's params (<Method>Params) and ack result (<Method>Result), from the
// canonical switchblade wire schemas in porting-sdk/relay-protocol/ (one
// standalone JSON-Schema file per method+phase, "<method>.{params,result}.json",
// draft-2020-12, extracted from the C# switchblade classes).
//
// This is the Go realization of the reference generator's generate_relay_protocol
// (porting-sdk/scripts/generate_python_rest_types.py) and the TypeScript
// generateForRelay (signalwire-typescript/scripts/generate-rest-types.ts). It is
// one of the fixed 5 cross-port generator commands (generate_rest,
// generate_rest_tests, generate_relay_protocol, generate_swaig_payloads,
// generate_swml_verbs); the Go realization splits them into separate cmd/ dirs so
// every port exposes the same 5 GEN-FRESH gates.
//
// The declaration follows the same object-vs-alias split as the REST types
// emitter: an object schema WITH properties becomes a Go struct (typed fields,
// json wire-key tags, optional -> *T); an object schema with NO properties (or a
// non-object) becomes a `type X map[string]any` alias — so the three empty-object
// methods (calling.call params/result, signalwire.disconnect result) are aliases,
// NOT surface structs, exactly matching the reference (which emits them as
// `TypeAlias = dict[str, Any]`, keeping the surface at 123 structs).
//
// GEN-FRESH-gated: `--check` reproduces the committed output and exits non-zero
// if the file differs. Resolves porting-sdk via $PORTING_SDK or sibling.
//
// Usage:
//
//	go run ./cmd/generate-relay-protocol          # (re)write protocol_types_generated.go
//	go run ./cmd/generate-relay-protocol --check  # GEN-FRESH: fail if it is stale
package main

import (
	"flag"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/signalwire/signalwire-go/v3/cmd/internal/gen"
	"gopkg.in/yaml.v3"
)

// relayHeader is the generated-file banner (names this producing command).
const relayHeader = `// Code generated by cmd/generate-relay-protocol; DO NOT EDIT.
//
// AUTO-GENERATED from porting-sdk/relay-protocol/*.{params,result}.json — regenerate with:
//   go run ./cmd/generate-relay-protocol
//
// One Go type per RELAY WS method's params (<Method>Params) and ack result
// (<Method>Result), from the canonical switchblade wire schemas. Object schemas
// with properties become structs (json wire-key tags, optional fields -> *T);
// empty-object / non-object schemas become map[string]any aliases. STATIC-ONLY:
// at runtime each payload is a plain map; this layer just types the shapes.

package relay
`

// relayPhase pairs a filename phase suffix with the Go type-name suffix.
type relayPhase struct {
	phase  string // "params" / "result" (the "<method>.<phase>.json" tail)
	suffix string // "Params" / "Result"
}

// emitRelayProtocolFile reads every relay-protocol/*.{params,result}.json schema
// and returns the generated pkg/relay/protocol_types_generated.go source (gofmt'd
// by the caller). Returns "" when the relay-protocol dir has no schemas.
func emitRelayProtocolFile(psdk string) (string, error) {
	relayDir := filepath.Join(psdk, "relay-protocol")
	entries, err := os.ReadDir(relayDir)
	if err != nil {
		if os.IsNotExist(err) {
			return "", nil
		}
		return "", err
	}
	byName := map[string]string{} // file base name -> full path
	for _, e := range entries {
		if e.IsDir() {
			continue
		}
		byName[e.Name()] = filepath.Join(relayDir, e.Name())
	}

	var b strings.Builder
	b.WriteString(relayHeader)
	b.WriteString("\n")

	emitted := 0
	// Iterate params files first, then result files — sorted within each phase — to
	// reproduce the reference decl order (Params block, then Result block).
	for _, ph := range []relayPhase{{"params", "Params"}, {"result", "Result"}} {
		suffixDot := "." + ph.phase + ".json"
		var files []string
		for name := range byName {
			if strings.HasSuffix(name, suffixDot) {
				files = append(files, name)
			}
		}
		sort.Strings(files)
		for _, name := range files {
			raw, err := os.ReadFile(byName[name])
			if err != nil {
				return "", err
			}
			var doc yaml.Node
			if err := yaml.Unmarshal(raw, &doc); err != nil {
				return "", fmt.Errorf("relay-protocol %s: %w", name, err)
			}
			node := rootOf(&doc)
			method := scalarChild(node, "x-method")
			if method == "" {
				method = strings.TrimSuffix(name, suffixDot)
			}
			goName := pascal(strings.ReplaceAll(method, ".", "_")) + ph.suffix
			if isObjectSchema(node) {
				emitStructDecl(&b, nil, goName, node)
			} else {
				fmt.Fprintf(&b, "type %s %s\n\n", goName, goFieldType(nil, node))
			}
			emitted++
		}
	}
	if emitted == 0 {
		return "", nil
	}
	return b.String(), nil
}

func run() error {
	check := flag.Bool("check", false, "GEN-FRESH: exit non-zero if the generated file is stale")
	flag.Parse()

	cwd, err := os.Getwd()
	if err != nil {
		return err
	}
	repoRoot, err := gen.FindRepoRoot(cwd)
	if err != nil {
		return err
	}
	psdk, err := gen.ResolvePortingSDK(repoRoot, "relay-protocol")
	if err != nil {
		if *check {
			return fmt.Errorf("generate-relay-protocol --check: %w", err)
		}
		fmt.Fprintf(os.Stderr, "generate-relay-protocol: %v — skipping (committed files kept)\n", err)
		return nil
	}

	src, err := emitRelayProtocolFile(psdk)
	if err != nil {
		return err
	}
	if src == "" {
		// No relay-protocol schemas — keep the committed file.
		return nil
	}
	out := gen.Output{
		Path: filepath.Join(repoRoot, "pkg", "relay", "protocol_types_generated.go"),
		Src:  src,
	}
	return gen.Run(*check, "generate-relay-protocol", "relay protocol", []gen.Output{out})
}

func main() {
	if err := run(); err != nil {
		fmt.Fprintf(os.Stderr, "error: %v\n", err)
		os.Exit(1)
	}
}
