// RELAY-protocol types emitter for cmd/generate-rest.
//
// Emits pkg/relay/protocol_types_generated.go (package relay): one Go type per
// RELAY WS method's params (<Method>Params) and ack result (<Method>Result),
// from the canonical switchblade wire schemas in porting-sdk/relay-protocol/
// (one standalone JSON-Schema file per method+phase, "<method>.{params,result}.json",
// draft-2020-12, extracted from the C# switchblade classes).
//
// This is the Go realization of the reference generator's generate_relay_protocol
// (porting-sdk/scripts/generate_python_rest_types.py) and the TypeScript
// generateForRelay (signalwire-typescript/scripts/generate-rest-types.ts,
// src/relay/protocol.types.generated.ts): iterate the params files then the result
// files, sorted; each file's type name is pascal(x-method.replace(".","_")) + the
// phase suffix ("Params"/"Result"), so calling.collect -> CallingCollectParams /
// CallingCollectResult. The declaration follows the same object-vs-alias split as
// the REST types emitter (types.go): an object schema WITH properties becomes a Go
// struct (typed fields, json wire-key tags, optional -> *T); an object schema with
// NO properties (or a non-object) becomes a `type X map[string]any` alias — so the
// three empty-object methods (calling.call params/result, signalwire.disconnect
// result) are aliases, NOT surface structs, exactly matching the reference (which
// emits them as `TypeAlias = dict[str, Any]`, keeping the surface at 123 structs).
//
// The relay JSON schemas carry no $ref (every nested object is inline), so the
// shared goFieldType walk collapses inline objects to map[string]any (matching TS
// Record<string,unknown> / Python dict[str, Any]) and needs no components node.
//
// GEN-FRESH-gated via the same --check flow as the REST types (run() below wires
// this emitter's output into the outs list).
package main

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"gopkg.in/yaml.v3"
)

// relayHeader is the generated-file banner (package relay).
const relayHeader = `// Code generated by cmd/generate-rest; DO NOT EDIT.
//
// AUTO-GENERATED from porting-sdk/relay-protocol/*.{params,result}.json — regenerate with:
//   go run ./cmd/generate-rest
//
// One Go type per RELAY WS method's params (<Method>Params) and ack result
// (<Method>Result), from the canonical switchblade wire schemas. Object schemas
// with properties become structs (json wire-key tags, optional fields -> *T);
// empty-object / non-object schemas become map[string]any aliases. STATIC-ONLY:
// at runtime each payload is a plain map; this layer just types the shapes.

package relay
`

// relayPhase pairs a filename phase suffix with the Go type-name suffix.
type relayPhase struct {
	phase  string // "params" / "result" (the "<method>.<phase>.json" tail)
	suffix string // "Params" / "Result"
}

// emitRelayProtocolFile reads every relay-protocol/*.{params,result}.json schema
// and returns the generated pkg/relay/protocol_types_generated.go source (gofmt'd
// by the caller). Returns "" when the relay-protocol dir has no schemas.
func emitRelayProtocolFile(psdk string) (string, error) {
	relayDir := filepath.Join(psdk, "relay-protocol")
	entries, err := os.ReadDir(relayDir)
	if err != nil {
		// No relay-protocol dir — nothing to emit (the driver keeps the committed
		// file). Any other read error is a real fault and propagates.
		if os.IsNotExist(err) {
			return "", nil
		}
		return "", err
	}
	byName := map[string]string{} // file base name -> full path
	for _, e := range entries {
		if e.IsDir() {
			continue
		}
		byName[e.Name()] = filepath.Join(relayDir, e.Name())
	}

	var b strings.Builder
	b.WriteString(relayHeader)
	b.WriteString("\n")

	emitted := 0
	// Iterate params files first, then result files — sorted within each phase — to
	// reproduce the reference decl order (Params block, then Result block).
	for _, ph := range []relayPhase{{"params", "Params"}, {"result", "Result"}} {
		suffixDot := "." + ph.phase + ".json"
		var files []string
		for name := range byName {
			if strings.HasSuffix(name, suffixDot) {
				files = append(files, name)
			}
		}
		sort.Strings(files)
		for _, name := range files {
			raw, err := os.ReadFile(byName[name])
			if err != nil {
				return "", err
			}
			var doc yaml.Node
			if err := yaml.Unmarshal(raw, &doc); err != nil {
				return "", fmt.Errorf("relay-protocol %s: %w", name, err)
			}
			node := rootOf(&doc)
			// The Go type name: x-method (fallback to the file stem) with dots -> "_",
			// PascalCased, plus the phase suffix. calling.collect -> CallingCollectParams.
			method := scalarChild(node, "x-method")
			if method == "" {
				method = strings.TrimSuffix(name, suffixDot)
			}
			goName := pascal(strings.ReplaceAll(method, ".", "_")) + ph.suffix
			// Same object-vs-alias split as the REST types emitter: an object WITH
			// properties -> struct; everything else (empty object, scalar, union) ->
			// a map[string]any / goFieldType alias. The relay schemas carry no $ref,
			// so a nil components node is fine (goFieldType only touches it for $refs).
			if isObjectSchema(node) {
				emitStructDecl(&b, nil, goName, node)
			} else {
				fmt.Fprintf(&b, "type %s %s\n\n", goName, goFieldType(nil, node))
			}
			emitted++
		}
	}
	if emitted == 0 {
		return "", nil
	}
	return b.String(), nil
}
